<h1>Описание API приложения</h1>

<p>Данная страница описывает API, доступный в приложении, включая маршруты для аутентификации пользователей, регистрации и восстановления пароля.</p>

<h2>Маршруты API</h2>

<ul>
    <li>
        <strong>Авторизация пользователя:</strong> <br>
        <code>http://localhost:8001/api/user</code> <br>
        <em>Метод:</em> GET <br>
        <em>Описание:</em> Возвращает информацию о текущем авторизованном пользователе. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Выход пользователя:</strong> <br>
        <code>http://localhost:8001/api/logout</code> <br>
        <em>Метод:</em> GET <br>
        <em>Описание:</em> Завершает сессию текущего пользователя. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Вход пользователя:</strong> <br>
        <code>http://localhost:8001/api/login</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Позволяет пользователю войти в систему. Доступен только для неавторизованных пользователей.
    </li>
    <li>
        <strong>Регистрация пользователя:</strong> <br>
        <code>http://localhost:8001/api/register</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Позволяет новому пользователю зарегистрироваться в системе. Доступен только для неавторизованных пользователей.
    </li>
    <li>
        <strong>Восстановление пароля:</strong> <br>
        <code>http://localhost:8001/api/restore</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Отправляет запрос на восстановление пароля. Доступен только для неавторизованных пользователей.
    </li>
    <li>
        <strong>Подтверждение восстановления пароля:</strong> <br>
        <code>http://localhost:8001/api/restore/confirm</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Подтверждает восстановление пароля. Доступен только для неавторизованных пользователей.
    </li>
    <li>
        <strong>Подтверждение email:</strong> <br>
        <code>http://localhost:8001/api/confirm_email</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Отправляет письмо для подтверждения email. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Создание пасты:</strong> <br>
        <code>http://localhost:8001/api/create_paste</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Позволяет пользователю создать новую пасту. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Удаление пасты:</strong> <br>
        <code>http://localhost:8001/api/delete_paste/{short_link}</code> <br>
        <em>Метод:</em> DELETE <br>
        <em>Описание:</em> Удаляет пасту по указанному короткому ссылке. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Обновление пасты:</strong> <br>
        <code>http://localhost:8001/api/update_paste/{short_link}</code> <br>
        <em>Метод:</em> PUT <br>
        <em>Описание:</em> Обновляет информацию о пасте по указанному короткому ссылке. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Комментирование пасты:</strong> <br>
        <code>http://localhost:8001/api/comment_paste/{short_link}</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Позволяет пользователю оставить комментарий к пасте. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Сообщение о пасте:</strong> <br>
        <code>http://localhost:8001/api/report_paste/{short_link}</code> <br>
        <em>Метод:</em> POST <br>
        <em>Описание:</em> Позволяет пользователю сообщить о проблеме с пастой. Доступен только для авторизованных пользователей.
    </li>
    <li>
        <strong>Получение комментариев к пасте:</strong> <br>
        <code>http://localhost:8001/api/comments_paste/{short_link}</code> <br>
        <em>Метод:</em> GET <br>
        <em>Описание:</em> Возвращает все комментарии к указанной пасте. Доступен для всех пользователей.
    </li>
    <li>
        <strong>Получение информации о пасте:</strong> <br>
        <code>http://localhost:8001/api/paste/{short_link}</code> <br>
        <em>Метод:</em> GET <br>
        <em>Описание:</em> Возвращает информацию о пасте по указанному короткому ссылке. Доступен для всех пользователей.
    </li>
    <li>
        <strong>Получение отчетов о пасте:</strong> <br>
        <code>http://localhost:8001/api/reports_paste/{short_link}</code> <br>
        <em>Метод:</em> GET <br>
        <em>Описание:</em> Возвращает все жалобы к пасте по указанному короткому ссылке. Доступен для всех пользователей.
    </li>
    <li>
        <strong>Получение всех паст:</strong> <br>
        <code>http://localhost:8001/api/pastes</code> <br>
        <em>Метод:</em> GET <br>
        <em>Описание:</em> Возвращает список всех паст. Доступен для всех пользователей.
    </li>
    <li>
        <strong>Получение паст пользователя:</strong> <br>
        <code>http://localhost:8001/api/user_pastes/{user_id}</code> <br>
        <em>Метод:</em> GET <br>
        <em>Описание:</em> Возвращает все пасты, созданные пользователем с указанным ID. Доступен для всех пользователей.
    </li>
</ul>

<h2>Примечания</h2>

<p>Все маршруты, требующие аутентификации, используют middleware <code>auth:api</code>, что обеспечивает безопасность и защиту данных пользователей.</p>
<p>Для работы с API необходимо использовать соответствующие HTTP-методы и передавать необходимые данные в теле запроса, если это требуется.</p>
